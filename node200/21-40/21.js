// typeof로 변수의 타입(형) 알아보기
const pi = 3.14;
const myName = 'xtring';

console.log('hello : %s', typeof 'hello');
console.log('"20" : %s', typeof '20');
console.log('pi : %s', typeof pi);
console.log('myName : %s', typeof myName);
console.log('30 : %s', typeof 30);
console.log('[] : %s', typeof []);
console.log('{} : %s', typeof {});
